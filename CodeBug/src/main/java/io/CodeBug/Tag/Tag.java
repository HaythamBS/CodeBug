package io.CodeBug.Tag;

public class Tag {

	private int tag_Id;
	private int name;
	
	
	public int getTag_Id() {
		return tag_Id;
	}
	public void setTag_Id(int tag_Id) {
		this.tag_Id = tag_Id;
	}
	public int getName() {
		return name;
	}
	public void setName(int name) {
		this.name = name;
	}
	
	
	
}
